function atBatEnd(
  spec: Omit<EventInsightSpec, "triggerTypes"> & {
    eventEquals?: string[];
    eventIncludes?: string[];
  },
): EventInsightSpec {
  return { ...spec, triggerTypes: ["at-bat-end"] };
}

function atBatStart(
  spec: Omit<EventInsightSpec, "triggerTypes">,
): EventInsightSpec {
  return { ...spec, triggerTypes: ["at-bat-start"] };
}

