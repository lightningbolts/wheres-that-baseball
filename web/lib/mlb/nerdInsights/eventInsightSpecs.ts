import type { LiveInsightContext, InsightTrigger } from "@/lib/mlb/nerdInsights/types";

export type InsightTeamSide = "offense" | "defense";
export type InsightPolarity = "elite" | "cursed" | "either";

export interface EventInsightSpec {
  statId: string;
  team: InsightTeamSide;
  polarity: InsightPolarity;
  triggerTypes: InsightTrigger["type"][];
  eliteMaxRank?: number;
  cursedBottomN?: number;
  /** Hand-crafted rules take priority when their statId matches. */
  skipIfHandCrafted?: boolean;
  match?: (ctx: LiveInsightContext) => boolean;
  eventEquals?: string[];
  eventIncludes?: string[];
  eyebrow: string;
  title: (ctx: LiveInsightContext, abbrev: string) => string;
  message: (
    ctx: LiveInsightContext,
    abbrev: string,
    statTitle: string,
    displayValue: string,
    rank: number,
  ) => string;
}

function offenseRuns(ctx: LiveInsightContext): number {
  return ctx.offenseTeamId === ctx.awayTeamId ? ctx.awayRuns : ctx.homeRuns;
}

function defenseRunsAllowed(ctx: LiveInsightContext): number {
  return ctx.defenseTeamId === ctx.awayTeamId ? ctx.awayRuns : ctx.homeRuns;
}

function totalRuns(ctx: LiveInsightContext): number {
  return ctx.awayRuns + ctx.homeRuns;
}

function runsInHalf(ctx: LiveInsightContext): number | null {
  if (ctx.trigger.type !== "half-break") return null;
  return ctx.liveStats?.runsByHalf[ctx.trigger.halfKey] ?? null;
}

function defaultMessage(
  _ctx: LiveInsightContext,
  abbrev: string,
  statTitle: string,
  displayValue: string,
  rank: number,
): string {
  return `${abbrev} rank #${rank} in ${statTitle.toLowerCase()} (${displayValue}).`;
}

